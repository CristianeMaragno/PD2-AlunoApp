package com.cristianerm.pd2;

public class UserInformation {
    String nome;
    String turma;

    public UserInformation() {

    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTurma() {
        return turma;
    }

    public void setTurma(String turma) {
        this.turma = turma;
    }
}
